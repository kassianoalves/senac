'''
1) Faça um programa que leia dois números e mostre a soma, a subtração, a multiplicação e a divisão entre eles. 

'''

num1 = int(input('Digite um valor: '))
num2 = int(input('Digite outro valor: '))

soma = num1 + num2
multiplicacao = num1 * num2
divisao =  num1 / num2
subtracao = num1 - num2

print (f'A soma dos valores é: {soma}')
print( f'A multiplicacao dos valores é: {multiplicacao}')
print(f'A divisão dos valores é: {divisao}')
print(f'A subtração dos valores é: {subtracao} ')