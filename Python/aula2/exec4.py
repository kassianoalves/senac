'''
 Crie um programa que leia três notas de um aluno e calcule a média aritmética.
'''

n1 = float(input('Digita a primeira nota: '))
n2 = float(input('Digita a segunda nota: '))
n3 = float(input('Digita a terceira nota: '))

media =  ( n1 + n2 + n3 ) / 3

print (f'A médio do aluno é: {media:.2f}')