'''
Antecessor e Sucessor: Escreva um programa que peça um número inteiro e mostre na tela o seu antecessor e o seu sucessor.
'''

numero = int(input("Digite um número inteiro: "))

antecessor = numero - 1 
sucessor = numero + 1

print (f'O antecessor de {numero} é {antecessor} e o sucessor é {sucessor}.')